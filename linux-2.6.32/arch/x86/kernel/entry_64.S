	/*
	 * "Paranoid" exit path from exception stack.
	 * Paranoid because this is used by NMIs and cannot take
	 * any kernel state for granted.
	 * We don't do kernel preemption checks here, because only
	 * NMI should be common and it does not enable IRQs and
	 * cannot get reschedule ticks.
	 *
	 * "trace" is 0 for the NMI handler only, because irq-tracing
	 * is fundamentally NMI-unsafe. (we cannot change the soft and
	 * hard flags at once, atomically)
	 *
	 * Since the exception stacks are visible to userland, we can
	 * do the CR3 switch and swapgs before restoring the registers.
	 */

	/* ebx:	no swapgs flag */
ENTRY(paranoid_exit)
	INTR_FRAME
	DISABLE_INTERRUPTS(CLBR_NONE)
	TRACE_IRQS_OFF
	testl %ebx,%ebx				/* swapgs needed? */
	jnz paranoid_noswapgs_restore
	testl $3,CS(%rsp)
	jnz   paranoid_userspace
paranoid_swapgs_restore:
	TRACE_IRQS_IRETQ 0
	IBRS_EXIT_RESTORE_CLOBBER save_reg=%r13d
	RESTORE_CR3 scratch_reg=%rax save_reg=%r14
	SWAPGS_UNSAFE_STACK
	RESTORE_ALL 8
	/*
	 * If swapgs needs to be called, there is a pretty good chance that
	 * the exception happens right before returning to userspace. So
	 * clear the CPU buffers just in case.
	 */
	MDS_USER_CLEAR_CPU_BUFFERS
	jmp irq_return
paranoid_noswapgs_restore:
	TRACE_IRQS_IRETQ 0
	IBRS_EXIT_RESTORE_CLOBBER save_reg=%r13d
	RESTORE_CR3 scratch_reg=%rax save_reg=%r14
	RESTORE_ALL 8
	jmp irq_return
paranoid_userspace:
	GET_THREAD_INFO(%rcx)
	movl TI_flags(%rcx),%ebx
	andl $_TIF_WORK_MASK,%ebx
	jz paranoid_userspace_restore_all
	movq %rsp,%rdi			/* &pt_regs */
	call sync_regs
	movq %rax,%rsp			/* switch stack for scheduling */
	testl $_TIF_NEED_RESCHED,%ebx
	jnz paranoid_schedule
	movl %ebx,%edx			/* arg3: thread flags */
	TRACE_IRQS_ON
	ENABLE_INTERRUPTS(CLBR_NONE)
	xorl %esi,%esi 			/* arg2: oldset */
	movq %rsp,%rdi 			/* arg1: &pt_regs */
	call do_notify_resume
	DISABLE_INTERRUPTS(CLBR_NONE)
	TRACE_IRQS_OFF
	jmp paranoid_userspace
paranoid_schedule:
	TRACE_IRQS_ON
	ENABLE_INTERRUPTS(CLBR_ANY)
	call schedule
	DISABLE_INTERRUPTS(CLBR_ANY)
	TRACE_IRQS_OFF
	jmp paranoid_userspace
	CFI_ENDPROC
END(paranoid_exit)

/*
 * Exception entry point. This expects an error code/orig_rax on the stack.
 * returns in "no swapgs flag" in %ebx.
 */
ENTRY(error_entry)
	XCPT_FRAME
	CFI_ADJUST_CFA_OFFSET 15*8
	/* oldrax contains error code */
	cld
	movq_cfi rdi, RDI+8
	movq_cfi rsi, RSI+8
	movq_cfi rdx, RDX+8
	movq_cfi rcx, RCX+8
	movq_cfi rax, RAX+8
	movq_cfi  r8,  R8+8
	movq_cfi  r9,  R9+8
	movq_cfi r10, R10+8
	movq_cfi r11, R11+8
	movq_cfi rbx, RBX+8
	movq_cfi rbp, RBP+8
	movq_cfi r12, R12+8
	movq_cfi r13, R13+8
	movq_cfi r14, R14+8
	movq_cfi r15, R15+8
	xorl %ebx,%ebx
	testl $3,CS+8(%rsp)
	je error_kernelspace
error_swapgs:
	SWAPGS
	FENCE_SWAPGS_USER_ENTRY
	SWITCH_TO_KERNEL_CR3
	IBRS_ENTRY_CLOBBER /* no indirect jump allowed before IBRS */
	FILL_RETURN_BUFFER_CLOBBER /* no ret allowed before stuffing the RSB */
error_sti:
	/* Check to see if we're on the trampoline stack. */
	movq PER_CPU_VAR(init_tss + TSS_sp0), %rcx
	cmpq %rcx, %rsp
	ja 1f
	leaq -TSS_stack_size(%rcx), %rax
	cmpq %rsp, %rax
	ja 1f
	/*
	 * We're on the trampoline stack.  Switch to the kernel task stack
	 * and copy the trampoline stack's contents to it.
	 */
	subq %rsp, %rcx
	movq PER_CPU_VAR(kernel_stack), %rdi
	addq $KERNEL_STACK_OFFSET, %rdi
	subq %rcx, %rdi
	movq %rsp, %rsi
	movq %rdi, %rsp
	rep movsb
1:
	TRACE_IRQS_OFF
	ret
	CFI_ENDPROC

/*
 * There are two places in the kernel that can potentially fault with
 * usergs. Handle them here. The exception handlers after iret run with
 * kernel gs again, so don't set the user space flag. B stepping K8s
 * sometimes report an truncated RIP for IRET exceptions returning to
 * compat mode. Check for these here too.
 */
error_kernelspace:
	incl %ebx
	leaq irq_return(%rip),%rcx
	cmpq %rcx,RIP+8(%rsp)
	je error_swapgs
	movl %ecx,%ecx	/* zero extend */
	cmpq %rcx,RIP+8(%rsp)
	je error_swapgs
	cmpq $gs_change,RIP+8(%rsp)
	je error_swapgs
	FENCE_SWAPGS_KERNEL_ENTRY
	jmp error_sti
END(error_entry)


/* ebx:	no swapgs flag (1: don't need swapgs, 0: need it) */
ENTRY(error_exit)
	DEFAULT_FRAME
	movl %ebx,%eax
	RESTORE_REST
	DISABLE_INTERRUPTS(CLBR_NONE)
	TRACE_IRQS_OFF
	GET_THREAD_INFO(%rcx)
	testl %eax,%eax
	jne retint_kernel
	LOCKDEP_SYS_EXIT_IRQ
	movl TI_flags(%rcx),%edx
	movl $_TIF_WORK_MASK,%edi
	andl %edi,%edx
	jnz retint_careful
	jmp retint_swapgs
	CFI_ENDPROC
END(error_exit)


	/* runs on exception stack */
ENTRY(nmi)
	INTR_FRAME
	PARAVIRT_ADJUST_EXCEPTION_FRAME

	testb	$3, CS-RIP(%rsp)
	jz	.Lnmi_from_kernel

	/*
	 * NMI from user mode.  We need to run on the thread stack, but we
	 * can't go through the normal entry paths: NMIs are masked, and
	 * we don't want to enable interrupts, because then we'll end
	 * up in an awkward situation in which IRQs are on but NMIs
	 * are off.
	 *
	 * We also must not push anything to the stack before switching
	 * stacks lest we corrupt the "NMI executing" variable.
	 */

	/* Use %rsi as out temp variable throughout */
	pushq	%rsi
	SWAPGS_UNSAFE_STACK
	FENCE_SWAPGS_USER_ENTRY
	SWITCH_TO_KERNEL_CR3
	cld
	movq	%rsp, %rsi
	movq	PER_CPU_VAR(kernel_stack), %rsp
	addq	$KERNEL_STACK_OFFSET, %rsp
	pushq	5*8(%rsi)	/* pt_regs->ss */
	pushq	4*8(%rsi)	/* pt_regs->rsp */
	pushq	3*8(%rsi)	/* pt_regs->flags */
	pushq	2*8(%rsi)	/* pt_regs->cs */
	pushq	1*8(%rsi)	/* pt_regs->rip */
	pushq   $-1		/* pt_regs->orig_ax */
	pushq   %rdi		/* pt_regs->di */
	pushq   (%rsi)		/* pt_regs->si */
	pushq   %rdx		/* pt_regs->dx */
	pushq   %rcx		/* pt_regs->cx */
	pushq   %rax		/* pt_regs->ax */
	pushq   %r8		/* pt_regs->r8 */
	pushq   %r9		/* pt_regs->r9 */
	pushq   %r10		/* pt_regs->r10 */
	pushq   %r11		/* pt_regs->r11 */
	pushq	%rbx		/* pt_regs->rbx */
	pushq	%rbp		/* pt_regs->rbp */
	pushq	%r12		/* pt_regs->r12 */
	pushq	%r13		/* pt_regs->r13 */
	pushq	%r14		/* pt_regs->r14 */
	pushq	%r15		/* pt_regs->r15 */

	IBRS_ENTRY_CLOBBER /* no indirect jump allowed before IBRS */
	FILL_RETURN_BUFFER_CLOBBER /* no ret allowed before stuffing the RSB */

	movq	%rsp, %rdi
	movq	$-1, %rsi
	call	do_nmi

	/*
	 * Return back to user mode.  We must *not* do the normal exit
	 * work, because we don't want to enable interrupts.  Fortunately,
	 * do_nmi doesn't modify pt_regs.
	 */
	addq	$6*8, %rsp	/* skip bx, bp, and r12-r15 */
	/*
	 * The kernel_stack to trampoline stack switching isn't safe
	 * with interrupt enabled.
	 */
	DISABLE_INTERRUPTS(CLBR_NONE)
	jmp	retint_userspace_restore_args

.Lnmi_from_kernel:

	pushq_cfi $-1
	subq $15*8, %rsp
	CFI_ADJUST_CFA_OFFSET 15*8
	call save_paranoid
	DEFAULT_FRAME 0
	/* paranoidentry do_nmi, 0; without TRACE_IRQS_OFF */
	movq %rsp,%rdi
	movq $-1,%rsi
	call do_nmi
	/* paranoidexit; without TRACE_IRQS_OFF */
	/* ebx:	no swapgs flag */
	DISABLE_INTERRUPTS(CLBR_NONE)
	IBRS_EXIT_RESTORE_CLOBBER save_reg=%r13d
	RESTORE_CR3 scratch_reg=%rax save_reg=%r14
	testl %ebx,%ebx				/* swapgs needed? */
	jnz nmi_noswapgs
	SWAPGS_UNSAFE_STACK
	/*
	 * If swapgs needs to be called, there is a pretty good chance that
	 * the NMI happens right before returning to userspace. So clear
	 * the CPU buffers just in case.
	 */
	MDS_USER_CLEAR_CPU_BUFFERS
nmi_noswapgs:
	RESTORE_ALL 8
	jmp irq_return
	CFI_ENDPROC
END(nmi)

